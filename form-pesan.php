function rubahtgl(strtgl,indHour)
		{
			var arrTgl=strtgl.split("-");
			var value="";
			if(indHour==0)
			{
				value=arrTgl[2]+"/"+arrTgl[1]+"/"+arrTgl[0];
			}
			else if(indHour==1)
			{
				var pemisahJam=arrTgl[2].split(" ");
				value=pemisahJam[0]+"/"+arrTgl[1]+"/"+arrTgl[0]+" "+pemisahJam[1];
			}
			else if(indHour==2)
			{
				var pemisahJam=arrTgl[2].split(" ");
				value=pemisahJam[0]+"/"+arrTgl[1]+"/"+arrTgl[0];
			}
			return value;
				
		}
